export interface Job {
  companyName: string;
  role: string;
  description: string;
  eligibility: string;
  cgpa: string;
  skills: string;
  logoUrl: string;
  packageLPA: number;
  type: 'Full-Time' | 'Internship';
  category: 'Software' | 'Hardware';
  status: 'Active' | 'Closed';
  applyLink: string;
}

export const parseJobListings = (listings: string): Job[] => {
  const jobs: Job[] = [];
  const entries = listings.trim().split('---').filter(entry => entry.trim() !== '');

  for (const entry of entries) {
    const lines = entry.trim().split('\n');
    const job: Partial<Job> = { 
        logoUrl: 'https://picsum.photos/64/64',
        packageLPA: 0,
        type: 'Full-Time',
        category: 'Software',
        status: 'Active',
        applyLink: '#',
    };

    for (const line of lines) {
      if (line.startsWith('Company:')) {
        job.companyName = line.replace('Company:', '').trim();
      } else if (line.startsWith('Role:')) {
        job.role = line.replace('Role:', '').trim();
      } else if (line.startsWith('Description:')) {
        job.description = line.replace('Description:', '').trim();
      } else if (line.startsWith('Eligibility:')) {
        job.eligibility = line.replace('Eligibility:', '').trim();
      } else if (line.startsWith('CGPA:')) {
        job.cgpa = line.replace('CGPA:', '').trim();
      } else if (line.startsWith('Skills:')) {
        job.skills = line.replace('Skills:', '').trim();
      } else if (line.startsWith('Logo:')) {
        const logo = line.replace('Logo:', '').trim();
        if (logo) job.logoUrl = logo;
      } else if (line.startsWith('Package (LPA):')) {
        job.packageLPA = parseFloat(line.replace('Package (LPA):', '').trim()) || 0;
      } else if (line.startsWith('Type:')) {
        job.type = line.replace('Type:', '').trim() as Job['type'];
      } else if (line.startsWith('Category:')) {
        job.category = line.replace('Category:', '').trim() as Job['category'];
      } else if (line.startsWith('Status:')) {
        job.status = line.replace('Status:', '').trim() as Job['status'];
      } else if (line.startsWith('Apply Link:')) {
        job.applyLink = line.replace('Apply Link:', '').trim();
      }
    }
    if (job.companyName) {
      jobs.push(job as Job);
    }
  }
  return jobs;
};

export const parsedJobListings = parseJobListings(jobListings);
